    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
       // Get the selected product name from the combo box
    String selectedProductName = (String) productToBeUpdated.getSelectedItem();

    // Get the new product name, price, and quantity from text fields
    String newProductNameStr = newProductName.getText();
    double newPriceValue = Double.parseDouble(newPrice.getText());
    int newQuantityValue = Integer.parseInt(newQuantity.getText());

    // Check if there's a selected product name
    if (selectedProductName == null || selectedProductName.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please select a product to update.");
        return;
    }

    // Check if the new product name, price, and quantity are valid
    if (newProductNameStr.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please enter a new product name.");
        return;
    }

    // Select the first image from the list as the new product image
    ImageIcon newProductImage = selectedImages.get(0);

    // Check if a new image is selected
    if (newProductImage == null) {
        JOptionPane.showMessageDialog(null, "Please select a new product image.");
        return;
    }

    // Create an instance of ProductManageable
    ProductManageable productManageable = new ProductManageable(null, null, 0, 0.0, " ");

    // Update the product in the database
    productManageable.updateProduct(selectedProductName, newProductNameStr, 
                                     imageToByteArray(newProductImage), newPriceValue, newQuantityValue);
      
    // Show a success message
    JOptionPane.showMessageDialog(null, "Product updated successfully.");
showSuppliers();
        supplieremails();
        displayProd();
        prodNames();
        showOders();
    // Clear the input fields
    newProductName.setText("");
    newPrice.setText("");
    newQuantity.setText("");

    // Refresh the product names in the combo box
    productManageable.loadProductNamesIntoComboBox(productToBeUpdated);
    }//GEN-LAST:event_jButton7ActionPerformed

