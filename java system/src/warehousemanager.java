    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed

        String name = productName.getText();
        double price = Double.parseDouble(productPrice.getText());
        int quantity = Integer.parseInt(productQuantity.getText());
        String supplierName = String.valueOf(prodSupplier.getSelectedItem());

        // Check if all fields are filled

        // Select the first image from the list
        ImageIcon productImageIcon = selectedImages.get(0);

        // Convert the product image to bytes
        byte[] productImageBytes = imageToByteArray(productImageIcon);

        // Create a ProductManageable instance and add the product to the database
        ProductManageable productManageable = new ProductManageable(name, productImageBytes, quantity, price, supplierName);

        // Add the product to the database
        productManageable.addProduct(name, productImageBytes, price, quantity);

        // Show a success message
        JOptionPane.showMessageDialog(null, "Product added successfully.");
        showSuppliers();
        supplieremails();
        displayProd();
        prodNames();
        showOders();
        // Clear the input fields
        productName.setText("");
        productPrice.setText("");
        productQuantity.setText("");
        suppliername.setText("");
        productImage.setIcon(null); // Clear the product image
            
        // Remove the used image from the list if needed
        if (!selectedImages.isEmpty()) {
            selectedImages.remove(0);
        }
    }//GEN-LAST:event_jButton10ActionPerformed

    private void UploadimageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UploadimageActionPerformed
        JFileChooser fileChooser = new JFileChooser();

        // Set the file filter to restrict to image files
        FileNameExtensionFilter imageFilter = new FileNameExtensionFilter("Image Files", "jpg", "jpeg", "png", "gif");
        fileChooser.setFileFilter(imageFilter);

        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            // User selected an image file
            File selectedFile = fileChooser.getSelectedFile();

            // Load the selected image and set it as the icon of your label
            BufferedImage image = null;
            try {
                image = ImageIO.read(selectedFile);
            } catch (IOException ex) {
                Logger.getLogger(warehousemanager.class.getName()).log(Level.SEVERE, null, ex);
            }
            ImageIcon imageIcon = new ImageIcon(image);
            productImage.setIcon(imageIcon);

            // Add the selected ImageIcon to the list
            selectedImages.add(imageIcon);}
    }//GEN-LAST:event_UploadimageActionPerformed

